"""LMA Core — shared protobuf wrapper library.

Re-exports generated protobuf stubs for LMAO message types
and the gRPC service stubs for K8s integration.
Both the server and host-side tests import from this package.

Requires protobuf stubs generated by either:
    bazel build //proto:lma_py_proto
or  python -m grpc_tools.protoc -I proto --python_out=proto --grpc_python_out=proto proto/lma.proto

Set PYTHONPATH to repo root when running outside Bazel.
"""

import logging

_logger = logging.getLogger(__name__)

try:
    from proto.lma_pb2 import (
        LMAOEnvelope,
        TextMessage,
        SensorReport,
        SensorReading,
        CommandRequest,
        CommandAck,
        AudioMessage,
        ImageMessage,
        CallSignal,
    )
except ImportError:
    _logger.critical(
        "Cannot import generated protobuf stubs from 'proto.lma_pb2'. "
        "Generate them with either:\n"
        "  Bazel:  bazel build //proto:lma_py_proto\n"
        "  Manual: protoc --python_out=. proto/lma.proto\n"
        "Then set PYTHONPATH to repo root when running outside Bazel."
    )
    raise

try:
    from proto.lma_pb2 import (
        SendRequest,
        SendResponse,
        SubscribeRequest,
        SubscribeResponse,
        TunnelRequest,
        TunnelResponse,
        GetIdentityRequest,
        GetIdentityResponse,
    )
except ImportError:
    _logger.warning(
        "gRPC request/response types not found in 'proto.lma_pb2'. "
        "K8s integration features will be unavailable."
    )

try:
    from proto.lma_pb2_grpc import (
        LMAOStub,
        LMAOServicer,
        LMAO,
    )
except ImportError:
    _logger.warning(
        "gRPC service stubs not found in 'proto.lma_pb2_grpc'. "
        "K8s integration features will be unavailable."
    )

__all__ = [
    # Core message types
    "LMAOEnvelope",
    "TextMessage",
    "SensorReport",
    "SensorReading",
    "CommandRequest",
    "CommandAck",
    "AudioMessage",
    "ImageMessage",
    "CallSignal",
]

# Conditionally add gRPC types to __all__ if they were imported
_grpc_request_types = [
    "SendRequest", "SendResponse",
    "SubscribeRequest", "SubscribeResponse",
    "TunnelRequest", "TunnelResponse",
    "GetIdentityRequest", "GetIdentityResponse",
]
__all__ += [name for name in _grpc_request_types if name in dir()]

_grpc_service_types = [
    "LMAOStub", "LMAOServicer", "LMAO",
]
__all__ += [name for name in _grpc_service_types if name in dir()]
