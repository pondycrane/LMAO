"""LMA Core — shared protobuf wrapper library.

Re-exports generated protobuf stubs for LMAO message types.
Both the server and host-side tests import from this package.

Requires protobuf stubs generated by Bazel.  Run
    bazel build //proto:lma_py_proto
before importing this module outside Bazel.
"""

import logging

_logger = logging.getLogger(__name__)

try:
    from proto.lma_pb2 import (
        LMAOEnvelope,
        TextMessage,
        SensorReport,
        SensorReading,
        CommandRequest,
        CommandAck,
        AudioMessage,
        ImageMessage,
        CallSignal,
    )
except ImportError:
    _logger.critical(
        "Cannot import generated protobuf stubs from 'proto.lma_pb2'. "
        "Generate them with either:\n"
        "  Bazel:  bazel build //proto:lma_py_proto\n"
        "  Manual: protoc --python_out=. proto/lma.proto\n"
        "Then set PYTHONPATH to repo root when running outside Bazel."
    )
    raise

__all__ = [
    "LMAOEnvelope",
    "TextMessage",
    "SensorReport",
    "SensorReading",
    "CommandRequest",
    "CommandAck",
    "AudioMessage",
    "ImageMessage",
    "CallSignal",
]
